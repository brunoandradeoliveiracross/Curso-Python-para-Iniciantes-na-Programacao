# Na programação as vezes você precisa saber se a informação é true ou false
# exemplo de operações que retorna true ou false

print (10 > 9) # 10 é maior que 9 retorna TRUE
print (10 == 9) # 10 Não é igual a 9 retorna FALSE
print (10 < 9) # 10 Não é menor a 9 retorna FALSE

# uma estrutura if else também retorna se o valor é verdadeiro ou falso

a = 10

if a > 11:
    print ("a é maior que 11") # Retorna este print se for TRUE
else:
    print ("a é menor que 11") # Retorna este print se for FALSE

